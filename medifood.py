def verify_security_manifest(signed_manifest_path, key_path):
    with open(key_path, "rb") as f:
        key = f.read()

    with open(signed_manifest_path, "r", encoding="utf-8") as f:
        signed = json.load(f)

    loaded_manifest = signed["manifest"]
    stored_signature = signed["manifest_signature_hmac_sha256"]

    recomputed_signature = hmac.new(
        key,
        canonical_json_bytes(loaded_manifest),
        hashlib.sha256
    ).hexdigest()

    signature_valid = hmac.compare_digest(stored_signature, recomputed_signature)

    file_results = []

    for item in loaded_manifest["protected_files"]:
        path = item["path"]
        expected_hash = item["sha256"]

        if not os.path.exists(path):
            file_results.append({
                "logical_name": item["logical_name"],
                "path": path,
                "exists": False,
                "expected_sha256": expected_hash,
                "current_sha256": None,
                "hash_match": False
            })
            continue

        current_hash = sha256_file(path)

        file_results.append({
            "logical_name": item["logical_name"],
            "path": path,
            "exists": True,
            "expected_sha256": expected_hash,
            "current_sha256": current_hash,
            "hash_match": hmac.compare_digest(expected_hash, current_hash)
        })

    all_files_valid = all(row["hash_match"] for row in file_results)

    verification_result = {
        "verified_utc": datetime.now(timezone.utc).isoformat(),
        "signature_valid": signature_valid,
        "all_files_valid": all_files_valid,
        "overall_status": "VERIFIED_PROVENANCE" if signature_valid and all_files_valid else "FAILED_VERIFICATION",
        "file_results": file_results
    }

    return verification_result

