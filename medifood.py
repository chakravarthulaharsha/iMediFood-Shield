def extract_text_from_pdf(path):
    text_parts = []

    try:
        doc = fitz.open(path)

        # First try selectable PDF text
        for page in doc:
            page_text = page.get_text()
            if page_text:
                text_parts.append(page_text)

        text = "\n".join(text_parts).strip()

        if len(text) > 20:
            return text, "PDF selectable text extraction"

        # If no useful selectable text, render pages and OCR
        ocr_parts = []

        for page in doc:
            pix = page.get_pixmap(dpi=200)
            img_bytes = pix.tobytes("png")
            img = Image.open(io.BytesIO(img_bytes))
            ocr_text = pytesseract.image_to_string(img)
            ocr_parts.append(ocr_text)

        return "\n".join(ocr_parts).strip(), "PDF image OCR using Tesseract"

    except Exception as e:
        return f"OCR/PDF extraction error: {e}", "Extraction error"

def extract_text_from_image(path):
    try:
        img = Image.open(path)
        text = pytesseract.image_to_string(img)
        return text.strip(), "Image OCR using Tesseract"
    except Exception as e:
        return f"Image OCR error: {e}", "Extraction error"

def extract_prescription_text(file_path):
    if file_path is None:
        return "", "No file uploaded", ""

    path = str(file_path)
    ext = os.path.splitext(path)[1].lower()

    file_hash = sha256_file(path)

    if ext == ".pdf":
        text, method = extract_text_from_pdf(path)
    elif ext in [".png", ".jpg", ".jpeg", ".webp", ".bmp", ".tiff"]:
        text, method = extract_text_from_image(path)
    else:
        text = "Unsupported file type. Please upload PDF, PNG, JPG, JPEG, WEBP, BMP, or TIFF."
        method = "Unsupported file type"

    return text, method, file_hash

def clean_ocr_for_matching(text):
    text = str(text)
    text = text.replace("\n", " ")
    text = re.sub(r"\b\d+(\.\d+)?\s*(mg|mcg|g|ml|tablet|tablets|tab|tabs|capsule|capsules|cap|caps|daily|bid|tid|qid|qday|night|morning|evening)\b", " ", text, flags=re.I)
    text = re.sub(r"[^A-Za-z0-9\s\-]", " ", text)
    text = re.sub(r"\s+", " ", text)
    return text.strip()

def detect_medications_from_text(ocr_text, max_results=8, score_cutoff=82):
    cleaned = clean_ocr_for_matching(ocr_text)
    cleaned_norm = normalize_name(cleaned)

    detected = []

    # Exact substring match against DDID medication dictionary
    for norm_name, display_name in drug_norm_to_display.items():
        if len(norm_name) >= 4 and norm_name in cleaned_norm:
            detected.append({
                "Drug_Name": display_name,
                "match_type": "exact_substring",
                "score": 100
            })

    # Fuzzy matching from lines/chunks
    chunks = []
    raw_lines = str(ocr_text).splitlines()

    for line in raw_lines:
        line_clean = clean_ocr_for_matching(line)
        if len(line_clean) >= 4:
            chunks.append(line_clean)

    # Also create short windows from the cleaned text
    words = cleaned.split()
    for n in [1, 2, 3]:
        for i in range(max(0, len(words) - n + 1)):
            phrase = " ".join(words[i:i+n])
            if len(phrase) >= 4:
                chunks.append(phrase)

    for chunk in chunks[:500]:
        chunk_norm = normalize_name(chunk)
        if not chunk_norm:
            continue

        matches = process.extract(
            chunk_norm,
            drug_norm_choices,
            scorer=fuzz.WRatio,
            limit=3
        )

        for match_norm, score, _ in matches:
            if score >= score_cutoff:
                detected.append({
                    "Drug_Name": drug_norm_to_display[match_norm],
                    "match_type": "fuzzy",
                    "score": round(float(score), 2)
                })

    if not detected:
        return pd.DataFrame(columns=["Drug_Name", "match_type", "score"])

    detected_df = pd.DataFrame(detected)
    detected_df = detected_df.sort_values(["score", "Drug_Name"], ascending=[False, True])
    detected_df = detected_df.drop_duplicates(subset=["Drug_Name"], keep="first")
    detected_df = detected_df.head(max_results).reset_index(drop=True)

    return detected_df

