# ---------- Safety-first label priority ----------
label_priority = {
    "Avoid": 3,
    "Use caution": 2,
    "No known interaction in selected source": 1,
    "Insufficient evidence": 0
}

effect_priority = {
    "Harmful": 5,
    "Negative": 4,
    "Positive": 3,
    "Possible": 2,
    "No Effect": 1
}

df["label_priority"] = df["paper_label"].map(label_priority).fillna(0)
df["effect_priority"] = df["effect_clean"].map(effect_priority).fillna(0)

# ---------- First, create conflict report ----------
conflict_report = df.groupby("pair_key").agg(
    Drug_ID=("Drug_ID", "first"),
    Drug_Name=("Drug_Name", "first"),
    Food_Herb_ID=("Food_Herb_ID", "first"),
    Food_Herb_Name=("Food_Herb_Name", "first"),
    Type=("Type", "first"),
    evidence_row_count=("pair_key", "size"),
    n_effects=("effect_clean", lambda x: x.nunique()),
    ddid_effects_all=("effect_clean", lambda x: "; ".join(sorted(set(x.astype(str))))),
    n_paper_labels=("paper_label", lambda x: x.nunique()),
    paper_labels_all=("paper_label", lambda x: "; ".join(sorted(set(x.astype(str))))),
    relationship_sources=("Relationship_classification", lambda x: "; ".join(sorted(set(x.astype(str))))),
    pmid_count=("PMID", lambda x: sum(x.astype(str).str.strip().ne(""))),
    doi_count=("DOI", lambda x: sum(x.astype(str).str.strip().ne("")))
).reset_index()

conflict_report["has_effect_conflict"] = conflict_report["n_effects"].apply(lambda x: 1 if x > 1 else 0)
conflict_report["has_label_conflict"] = conflict_report["n_paper_labels"].apply(lambda x: 1 if x > 1 else 0)

conflict_path = os.path.join(processed_dir, "duplicate_pair_conflict_report.csv")
conflict_report.to_csv(conflict_path, index=False)

# ---------- Select strongest row per pair ----------
# Sort so strongest safety label comes first
df_sorted = df.sort_values(
    by=["pair_key", "label_priority", "effect_priority", "has_pmid", "has_doi", "has_result_text", "has_conclusion_text"],
    ascending=[True, False, False, False, False, False, False]
)

pair_level = df_sorted.drop_duplicates(subset=["pair_key"], keep="first").copy()

# ---------- Add pair-level evidence summary ----------
pair_level = pair_level.merge(
    conflict_report[[
        "pair_key",
        "evidence_row_count",
        "n_effects",
        "ddid_effects_all",
        "n_paper_labels",
        "paper_labels_all",
        "relationship_sources",
        "pmid_count",
        "doi_count",
        "has_effect_conflict",
        "has_label_conflict"
    ]],
    on="pair_key",
    how="left"
)

# ---------- Remove helper priority columns ----------
pair_level = pair_level.drop(columns=["label_priority", "effect_priority"], errors="ignore")

# ---------- Save pair-level dataset ----------
