    gated_pred = pred_labels.copy()

    # Safety gate:
    # if model predicts "No known interaction" but confidence is below threshold,
    # change output to "Use caution"
    mask = (pred_labels == no_known_label) & (no_known_probs < threshold)
    gated_pred[mask] = "Use caution"

    return gated_pred, probs

