def preprocess_image_for_ocr(path):
    img = Image.open(path).convert("L")

    # Enlarge image for better OCR
    w, h = img.size
    img = img.resize((w * 3, h * 3))

    # Improve contrast and sharpness
    img = ImageOps.autocontrast(img)
    img = img.filter(ImageFilter.SHARPEN)

    # Threshold
    img = img.point(lambda x: 0 if x < 160 else 255, "1")

    return img

def extract_text_from_image(path):
    try:
        img = preprocess_image_for_ocr(path)

        config = "--oem 3 --psm 6"
        text = pytesseract.image_to_string(img, config=config)

        return text.strip(), "Image OCR using Tesseract with preprocessing"
    except Exception as e:
        return f"Image OCR error: {e}", "Extraction error"

def clean_ocr_for_matching(text):
    text = str(text)
    text = text.replace("\n", " ")

    # Remove common prescription dosage/frequency terms
    text = re.sub(
        r"\b\d+(\.\d+)?\s*(mg|mcg|g|ml|tablet|tablets|tab|tabs|capsule|capsules|cap|caps)\b",
        " ",
        text,
        flags=re.I
    )

    text = re.sub(
        r"\b(take|one|once|twice|daily|night|morning|evening|before|after|food|needed|as|prn|tablet|capsule)\b",
        " ",
        text,
        flags=re.I
    )

    text = re.sub(r"[^A-Za-z0-9\s-]", " ", text)
    text = re.sub(r"\s+", " ", text)

    return text.strip()

def detect_medications_from_text(ocr_text, max_results=8, score_cutoff=85):
    cleaned = clean_ocr_for_matching(ocr_text)
    cleaned_norm = normalize_name(cleaned)

    stop_phrases = {
        "test", "user", "patient", "synthetic", "prescription",
        "medication", "real", "not", "name", "date", "doctor",
        "rx", "sig", "directions"
    }

    detected = []

    # --------------------------------------------------
    # 1. Exact substring match
    # --------------------------------------------------
    for norm_name, display_name in drug_norm_to_display.items():
        if len(norm_name) >= 4 and norm_name in cleaned_norm:
            detected.append({
                "Drug_Name": display_name,
                "match_type": "exact_substring",
                "score": 100.0,
                "matched_text": norm_name
            })

    # --------------------------------------------------
    # 2. Fuzzy match using only best match per phrase
    # --------------------------------------------------
    chunks = []

    raw_lines = str(ocr_text).splitlines()

    for line in raw_lines:
        line_clean = clean_ocr_for_matching(line)
        line_clean = normalize_name(line_clean)

        if len(line_clean) >= 4:
            chunks.append(line_clean)

    words = clean_ocr_for_matching(ocr_text).split()

    for n in [1, 2, 3]:
        for i in range(max(0, len(words) - n + 1)):
            phrase = " ".join(words[i:i+n])
            phrase_norm = normalize_name(phrase)

            if len(phrase_norm) >= 4:
                chunks.append(phrase_norm)

    seen_chunks = set()

    for chunk_norm in chunks:
        if chunk_norm in seen_chunks:
            continue

        seen_chunks.add(chunk_norm)

        if chunk_norm in stop_phrases:
            continue

        # Avoid matching very generic fragments
        if len(chunk_norm) < 5:
            continue

        best_match = process.extractOne(
            chunk_norm,
            drug_norm_choices,
            scorer=fuzz.WRatio
        )

        if best_match is None:
            continue

        match_norm, score, _ = best_match

        # Avoid short fuzzy false positives such as "Iron" unless exact substring found
        if len(match_norm) < 6:
            continue

        # Avoid matching document header phrases to drug names
        bad_context_words = ["synthetic", "prescription", "patient", "test", "user", "medication"]
        if any(word in chunk_norm for word in bad_context_words):
            continue

        if score >= score_cutoff:
            detected.append({
                "Drug_Name": drug_norm_to_display[match_norm],
                "match_type": "fuzzy",
                "score": round(float(score), 2),
                "matched_text": chunk_norm
            })

    if not detected:
        return pd.DataFrame(columns=["Drug_Name", "match_type", "score", "matched_text"])

    detected_df = pd.DataFrame(detected)

    # Keep best score per detected drug
    detected_df = detected_df.sort_values(
        ["score", "match_type", "Drug_Name"],
        ascending=[False, True, True]
    )

    detected_df = detected_df.drop_duplicates(
        subset=["Drug_Name"],
        keep="first"
    )

    # Prefer exact matches first, then high-confidence fuzzy matches
    detected_df["match_rank"] = detected_df["match_type"].map({
        "exact_substring": 0,
        "fuzzy": 1
    }).fillna(2)

    detected_df = detected_df.sort_values(
        ["match_rank", "score"],
        ascending=[True, False]
    )

    detected_df = detected_df.head(max_results).reset_index(drop=True)

    return detected_df[["Drug_Name", "match_type", "score", "matched_text"]]

