def slugify(text):
    text = text.lower()
    text = re.sub(r"[^a-z0-9]+", "_", text)
    text = text.strip("_")
    return text

def make_calibrated_linearsvc():
    base_svc = LinearSVC(
        class_weight="balanced",
        random_state=42,
        max_iter=5000
    )

    try:
        return CalibratedClassifierCV(
            estimator=base_svc,
            method="sigmoid",
            cv=3
        )
    except TypeError:
        return CalibratedClassifierCV(
            base_estimator=base_svc,
            method="sigmoid",
            cv=3
        )

def apply_no_known_safety_gate(model, X, threshold):
    probs = model.predict_proba(X)
    classes = list(model.classes_)

    no_known_label = "No known interaction in selected source"
    no_known_idx = classes.index(no_known_label)

    pred_idx = np.argmax(probs, axis=1)
    pred_labels = np.array([classes[i] for i in pred_idx])
    no_known_probs = probs[:, no_known_idx]

    gated_pred = pred_labels.copy()

    # Only allow "No known interaction" when confidence is high.
    mask = (pred_labels == no_known_label) & (no_known_probs < threshold)
    gated_pred[mask] = "Use caution"

    return gated_pred, probs, classes

def save_confusion_outputs(model_name, y_true, y_pred):
    cm = confusion_matrix(y_true, y_pred, labels=labels)

    cm_df = pd.DataFrame(
        cm,
        index=[f"True: {x}" for x in labels],
        columns=[f"Pred: {x}" for x in labels]
    )

    slug = slugify(model_name)

    cm_csv_path = os.path.join(results_dir, f"confusion_matrix_{slug}.csv")
    cm_png_path = os.path.join(results_dir, f"confusion_matrix_{slug}.png")

    cm_df.to_csv(cm_csv_path)

    disp = ConfusionMatrixDisplay(confusion_matrix=cm, display_labels=labels)
    fig, ax = plt.subplots(figsize=(9, 7))
    disp.plot(ax=ax, values_format="d")
    plt.title(model_name)
    plt.xticks(rotation=30, ha="right")
    plt.tight_layout()
    plt.savefig(cm_png_path, dpi=300, bbox_inches="tight")
    plt.show()

    print("Saved confusion matrix CSV:", cm_csv_path)
    print("Saved confusion matrix PNG:", cm_png_path)

    return cm_df, cm_csv_path, cm_png_path

