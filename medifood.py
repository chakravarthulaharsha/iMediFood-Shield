def sha256_file(path, chunk_size=8192):
    h = hashlib.sha256()
    with open(path, "rb") as f:
        while True:
            chunk = f.read(chunk_size)
            if not chunk:
                break
            h.update(chunk)
    return h.hexdigest()

def canonical_json_bytes(obj):
    return json.dumps(
        obj,
        sort_keys=True,
        separators=(",", ":"),
        ensure_ascii=False
    ).encode("utf-8")

def hmac_sign_record(record_body, key):
    return hmac.new(
        key,
        canonical_json_bytes(record_body),
        hashlib.sha256
    ).hexdigest()

def verify_signed_record(signed_record, key):
    record_body = signed_record["record_body"]
    stored_signature = signed_record["record_signature_hmac_sha256"]
    recomputed_signature = hmac_sign_record(record_body, key)
    return hmac.compare_digest(stored_signature, recomputed_signature)

def verify_security_manifest(signed_manifest_path, key_path):
    with open(key_path, "rb") as f:
        key = f.read()

    with open(signed_manifest_path, "r", encoding="utf-8") as f:
        signed = json.load(f)

    loaded_manifest = signed["manifest"]
    stored_signature = signed["manifest_signature_hmac_sha256"]

    recomputed_signature = hmac.new(
        key,
        canonical_json_bytes(loaded_manifest),
        hashlib.sha256
    ).hexdigest()

    signature_valid = hmac.compare_digest(stored_signature, recomputed_signature)

    file_results = []

    for item in loaded_manifest["protected_files"]:
        path = item["path"]
        expected_hash = item["sha256"]

        if not os.path.exists(path):
            file_results.append(False)
            continue

        current_hash = sha256_file(path)
        file_results.append(hmac.compare_digest(expected_hash, current_hash))

    all_files_valid = all(file_results)

    return signature_valid and all_files_valid

def timed_run(function, repeats=30):
    times = []
    outputs = []

    for _ in range(repeats):
        start = time.perf_counter()
        output = function()
        end = time.perf_counter()

        times.append(end - start)
        outputs.append(output)

    return {
        "mean_seconds": sum(times) / len(times),
        "min_seconds": min(times),
        "max_seconds": max(times),
        "runs": repeats,
        "last_output": outputs[-1]
    }

